def get_mdi_character(icon_name: str) -> str | None:
    """
    Get the Unicode character for an MDI icon name.

    Args:
        icon_name: The MDI icon name (with or without 'mdi:' prefix)

    Returns:
        The Unicode character for the icon, or None if not found
    """
    # Remove 'mdi:' prefix if present
    if icon_name.startswith("mdi:"):
        icon_name = icon_name[4:]

    # Return the character or None
    return MDI_CODEPOINTS.get(icon_name)


def is_mdi_icon(icon_name: str) -> bool:
    """Check if an icon name is an MDI icon (starts with 'mdi:')."""
    return isinstance(icon_name, str) and icon_name.startswith("mdi:")
